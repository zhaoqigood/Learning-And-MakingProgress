    // change decimal representation to the binaty representation
    private String changeToBinary16(short input){
        String output = ""; // the return value
        int result = input; // store the result of the modulus 2
        int remainder; // remainder
            for (int i = 0; i < 16; i++) {
                result = input >>> i;
                remainder = result & 0x1; // get the positive digit
                output = Integer.toString(remainder) + output;// get the digit
            }
        return output;

    }
    // change binary representation to decimal representation
    private short changeToDecimal(String input){
        int length = input.length(); // get the length of the string
        int result = 0; // get the highest digit of the string
        // calculte the decimal value of the binary representation
        for(int i = 0; i < length; i++){
            result = result * 2 + Character.getNumericValue(input.charAt(i));
        }
        return (short)result;

    }
