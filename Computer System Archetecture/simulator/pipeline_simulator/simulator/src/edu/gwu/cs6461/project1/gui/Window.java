    private void deleteCharacter(){
        if(permission_Input){
            // choose the corresponding area to delete the character
            if(choose_InputArea){
                String s = input_TextArea.getText();
                int length = s.length();
                if(length > 0){
                    s=s.substring(0,length-1);
                }
                input_TextArea.setText(s);
            }
            else{
                String s = input_TextField.getText();
                int length = s.length();
                if(length > 0){
                    s=s.substring(0,length-1);
                }
                input_TextField.setText(s);
            }
        }
    }
